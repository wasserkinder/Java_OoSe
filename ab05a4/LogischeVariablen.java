package ab05a4;

public class LogischeVariablen {
	
	private boolean a;
	private boolean b;

	public LogischeVariablen(boolean a, boolean b) {
		
		super();
		this.a = a;
		
		this.b = b;
	}

	public boolean getA() {
		
		return a;
	}

	public boolean getB() {
		
		return b;
	}

	public void setA(boolean a) {
		
		this.a = a;
	}

	public void setB(boolean b) {
		
		this.b = b;
		
	}

}
